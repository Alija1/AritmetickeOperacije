var a = prompt("Please enter number A:");
var b = prompt("Please enter number B:");
var c = prompt("Please enter an arithmetic operator:");
var result;

if (c = "+"){result = parseInt(a) + parseInt(b);
alert("Result is: " + result);
}

if (c = "-"){result = parseInt(a) - parseInt(b);
    alert("Result is: " + result);
}

if (c = "*"){result = parseInt(a) * parseInt(b);
    alert("Result is: " + result);
}

if (c = "/"){result = parseInt(a) / parseInt(b);
    alert("Result is: " + result);
}