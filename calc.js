let a = prompt("Please enter number A:");
let c = prompt("Please enter an arithmetic operator:");
let b = prompt("Please enter number B:");
let result;

if (c = "+"){result = parseInt(a) + parseInt(b);
alert("The result is: " + result);
}

else if (c = "-"){result = parseInt(a) - parseInt(b);
    alert("The result is: " + result);
}

else if (c = "*"){result = parseInt(a) * parseInt(b);
    alert("The result is: " + result);
}

else if (c = "/"){result = parseInt(a) / parseInt(b);
    alert("The result is: " + result);
}